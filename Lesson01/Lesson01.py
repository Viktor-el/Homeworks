# 4.    Пользователь вводит целое положительное число. Найдите самую большую цифру в числе.
#       Для решения используйте цикл while и арифметические операции.

number, maximum = int(input("Введите число: ")), 0
while number > 0:
    total = number % 10
    if total > maximum:
        maximum = total
    number //= 10
print(maximum)

# 5.    Запросите у пользователя значения выручки и издержек фирмы. Определите, с каким финансовым результатом
#       работает фирма (прибыль — выручка больше издержек, или убыток — издержки больше выручки).
#       Выведите соответствующее сообщение. Если фирма отработала с прибылью, вычислите рентабельность
#       выручки (соотношение прибыли к выручке).
#       Далее запросите численность сотрудников фирмы и определите прибыль фирмы в расчете на одного сотрудника.

x = int(input("Введите выручку фирмы: "))
y = int(input("Введите издержки фирмы: "))
if x > y:
    print("Фирма работает в прибыль")
    print("Рентабельность выручки:", int(round((x - y) / x, 2) * 100), "%")
    z = int(input("Введите количество сотрудников фирмы: "))
    print(f"Прибыль фирмы в расчете на одного сотрудника:, {(x - y) / z:.2f}")
elif x == y:
    print("Фирма работает в ноль")
else:
    print("Фирма работает в убыток")

# 6.    Спортсмен занимается ежедневными пробежками. В первый день его результат составил a километров.
#       Каждый день спортсмен увеличивал результат на 10 % относительно предыдущего. Требуется определить номер дня,
#       на который общий результат спортсмена составит не менее b километров.
#       Программа должна принимать значения параметров a и b и выводить одно натуральное число — номер дня.

a, b, c, total = int(input("Введите первое число: ")), int(input("Введите второе число: ")), 0, 1
while b > c:
    total += 1
    c = 1.1 * a
    a = c
print(total)
