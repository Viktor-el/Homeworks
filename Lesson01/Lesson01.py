# 2.    Пользователь вводит время в секундах. Переведите время в часы, минуты и секунды
#       и выведите в формате чч:мм:сс. Используйте форматирование строк.

time = int(input("Введите время в секундах: "))
time_format = int(input("Введите 24 (если 24-ёх часовой формат) или 12 (если 12-ти часовой формат): "))
if 0 <= time <= 86399 and time_format == 24 or time_format == 12:
    if time_format == 24:
        print(f"{time // 3600:02d}:{time % 3600 // 60:02d}:{time % 60:02d}")
    elif time_format == 12:
        if time <= 43199:
            print(f"{time // 3600:02d}:{time % 3600 // 60:02d}:{time % 60:02d} a.m.")
        else:
            time_0 = time - 43200
            print(f"{time_0 // 3600:02d}:{time_0 % 3600 // 60:02d}:{time_0 % 60:02d} p.m.")
else:
    print("Недопустипое значение времени (от 0 до 86399) либо формата (12 или 24)")

